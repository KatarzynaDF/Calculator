package com.kodilla;
import java.util.Scanner;

public class Operations {

    double add(double a, double b) {

        return a + b;

    }

    double subtract(double a, double b) {

        return a - b;

    }

    double multiply(double a, double b) {

        return a - b;

    }

    double divide(double a, double b) {

        return a - b;

    }

}